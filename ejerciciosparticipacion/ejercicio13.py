# Crear un programa que pida al usuario ingresar dos números y calcule su suma, resta, multiplicación y división.

numero1 = int(input("Ingrese el primer numero: "))
numero2 = int(input("Ingrese el segundo numero: "))

suma = numero1 + numero2
resta = numero1 - numero2
multiplicacion = numero1 * numero2
division= numero1 / numero2

print(f"Con los dos numeros puestos, su suma es {suma}, su resta es {resta}, su multiplicacion es {multiplicacion} y su division es {division}")

